//First Program in C
/* print different types of variables */

#include <stdio.h>
#define PI 3.1416f
int main()
{
   int i = 3;
   float f = 3.14;
   char c = 'U';

   printf("value of i is %i\n", i);
   printf("value of f is %f\n", f);
   printf("value of c is %c\n", c);
   printf("value of PI is %f\n", PI);
   return 0;
}
